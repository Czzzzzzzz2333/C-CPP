#include<stdio.h>
int main()
{
    long I,S;
    scanf("%ld",&I);
    if(I<=100000)
        S=I*0.1;
    if(I>100000&&I<=200000)
        S=100000*0.1+(I-100000)*0.075;
    if(I>200000&&I<=400000)
        S=200000*0.175+(I-200000)*0.05;
    if(I>400000&&I<=600000)
        S=400000*0.18+(I-400000)*0.03;
    if(I>600000&&I<=1000000)
        S=600000*0.21+(I-600000)*0.015;
    if(I>1000000)
        S=1000000*0.225+(I-1000000)*0.01;
    printf("%ld",S);
    return 0;
}
