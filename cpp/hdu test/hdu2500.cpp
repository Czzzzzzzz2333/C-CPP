#include <bits/stdc++.h>
using namespace std;
int main() {
  ios::sync_with_stdio(false);
  cin.tie(0);
  cout.tie(0);
  int n;
  cin >> n;
  while (n--) {
    int m;
    cin >> m;
    for (int i = 0; i < 3 * m; i++) {
      for (int j = 0; j < m; j++) {
        printf("HDU");
      }
      printf("\n");
    }
  }
  return 0;
}